        [Test]
        public async Task ModelFactoryMethodDoesNotKeepModelPublic()
        {
            MockHelpers.LoadMockGenerator();
            var workspace = new AdhocWorkspace();
            var projectInfo = ProjectInfo.Create(
                    ProjectId.CreateNewId(),
                    VersionStamp.Create(),
                    name: "TestProj",
                    assemblyName: "TestProj",
                    language: LanguageNames.CSharp)
                .WithMetadataReferences(new[]
                {
                    MetadataReference.CreateFromFile(typeof(object).Assembly.Location)
                });

            var project = workspace.AddProject(projectInfo);
            project = AddGeneratedDocument(project, "RootClient.cs", "src", "Generated", "RootClient.cs", """
namespace Sample
{
    public partial class RootClient
    {
    }
}
""");
            project = AddGeneratedDocument(project, "UnusedModel.cs", "src", "Generated", "Models", "UnusedModel.cs", """
namespace Sample.Models
{
    public partial class UnusedModel
    {
    }
}
""");
            project = AddGeneratedDocument(project, "SampleModelFactory.cs", "src", "Generated", "SampleModelFactory.cs", """
namespace Sample
{
    public static partial class SampleModelFactory
    {
        public static Sample.Models.UnusedModel UnusedModel() => new Sample.Models.UnusedModel();
    }
}
""");

            var postProcessor = new TestPostProcessor("RootClient.cs", modelFactoryFullName: "Sample.SampleModelFactory");

            var resultProject = await postProcessor.InternalizeAsync(project);
            var modelDoc = resultProject.Documents.Single(d => d.Name == "UnusedModel.cs");
            var modelRoot = await modelDoc.GetSyntaxRootAsync();
            var modelDeclaration = modelRoot!.DescendantNodes().OfType<ClassDeclarationSyntax>().Single();
            Assert.IsTrue(modelDeclaration.Modifiers.Any(m => m.Text == "internal"));
            Assert.IsFalse(modelDeclaration.Modifiers.Any(m => m.Text == "public"));

            var modelFactoryDoc = resultProject.Documents.SingleOrDefault(d => d.Name == "SampleModelFactory.cs");
            if (modelFactoryDoc != null)
            {
                var modelFactoryRoot = await modelFactoryDoc.GetSyntaxRootAsync();
                Assert.IsFalse(modelFactoryRoot!.DescendantNodes().OfType<MethodDeclarationSyntax>().Any(),
                    "Model factory methods for internalized models should be removed.");
            }
        }

        [TestCase("RequestBodyModel")]
        [TestCase("ResponseBodyModel")]
        [TestCase("NestedToolConfig")]
        [TestCase("BodyOnlyValueKind")]
        public async Task BodyOnlyGeneratedTypesDoNotBecomePublic(string bodyOnlyTypeName)
        {
            var project = CreateProjectWithRootClient();
            var typeDeclaration = bodyOnlyTypeName == "BodyOnlyValueKind"
                ? $$"""
namespace Sample.Models
{
    public enum {{bodyOnlyTypeName}}
    {
        Default
    }
}
"""
                : $$"""
namespace Sample.Models
{
    public partial class {{bodyOnlyTypeName}}
    {
    }
}
""";
            project = AddGeneratedDocument(project, $"{bodyOnlyTypeName}.cs", "src", "Generated", "Models", $"{bodyOnlyTypeName}.cs", typeDeclaration);
            var factoryExpression = bodyOnlyTypeName == "BodyOnlyValueKind"
                ? $"Sample.Models.{bodyOnlyTypeName}.Default"
                : $"new Sample.Models.{bodyOnlyTypeName}()";
            project = AddGeneratedDocument(project, "SampleModelFactory.cs", "src", "Generated", "SampleModelFactory.cs", $$"""
namespace Sample
{
    public static partial class SampleModelFactory
    {
        public static Sample.Models.{{bodyOnlyTypeName}} {{bodyOnlyTypeName}}() => {{factoryExpression}};
    }
}
""");

            var resultProject = await new TestPostProcessor("RootClient.cs", modelFactoryFullName: "Sample.SampleModelFactory").InternalizeAsync(project);

            await AssertGeneratedTypeInternal(resultProject, $"{bodyOnlyTypeName}.cs", bodyOnlyTypeName);
            await AssertModelFactoryMethodRemoved(resultProject, bodyOnlyTypeName);
        }

