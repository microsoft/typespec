        }

        [Test]
        public async Task ClientOptionsNestedEnumDoesNotBecomePublicWhenOptionsIsNotRooted()
        {
            var project = CreateProjectWithRootClient("public SampleClientOptions Options { get; }");
            project = AddGeneratedDocument(project, "SampleClientOptions.cs", "src", "Generated", "SampleClientOptions.cs", """
namespace Sample
{
    public partial class SampleClientOptions
    {
        public enum ServiceVersion
        {
            V1
        }
    }
}
""");

            var resultProject = await new TestPostProcessor("RootClient.cs").InternalizeAsync(project);

            await AssertGeneratedTypePublic(resultProject, "SampleClientOptions.cs", "SampleClientOptions");
            var optionsDoc = resultProject.Documents.Single(d => d.Name == "SampleClientOptions.cs");
            var root = await optionsDoc.GetSyntaxRootAsync();
            var nestedEnum = root!.DescendantNodes().OfType<EnumDeclarationSyntax>().Single();
            Assert.IsFalse(nestedEnum.Modifiers.Any(m => m.Text == "public"));
        }

