        private static async Task AssertGeneratedTypeInternal(Project project, string documentName, string typeName)
        {
            var declaration = await GetTypeDeclaration(project, documentName, typeName);
            Assert.IsTrue(declaration.Modifiers.Any(m => m.Text == "internal"), $"{typeName} should be internal.");
            Assert.IsFalse(declaration.Modifiers.Any(m => m.Text == "public"), $"{typeName} should not be public.");
        }

        private static async Task AssertGeneratedTypePublic(Project project, string documentName, string typeName)
        {
            var declaration = await GetTypeDeclaration(project, documentName, typeName);
            Assert.IsTrue(declaration.Modifiers.Any(m => m.Text == "public"), $"{typeName} should be public.");
        }

        private static async Task<BaseTypeDeclarationSyntax> GetTypeDeclaration(Project project, string documentName, string typeName)
        {
            var document = project.Documents.Single(d => d.Name == documentName);
            var root = await document.GetSyntaxRootAsync();
            return root!.DescendantNodes().OfType<BaseTypeDeclarationSyntax>().Single(t => t.Identifier.Text == typeName);
        }

        private static async Task AssertModelFactoryMethodRemoved(Project project, string methodName)
        {
            var modelFactoryDoc = project.Documents.SingleOrDefault(d => d.Name == "SampleModelFactory.cs");
            if (modelFactoryDoc == null)
            {
                return;
            }

            var modelFactoryRoot = await modelFactoryDoc.GetSyntaxRootAsync();
            Assert.IsFalse(modelFactoryRoot!.DescendantNodes().OfType<MethodDeclarationSyntax>().Any(m => m.Identifier.Text == methodName),
                $"Model factory method {methodName} should be removed.");
        }

        private class TestPostProcessor : PostProcessor
        {
            private readonly string _rootFile;

            public TestPostProcessor(string rootFile, IEnumerable<string>? additionalRootTypeNames = null, IEnumerable<string>? nonRootTypes = null, string? modelFactoryFullName = null) : base((additionalRootTypeNames ?? []).ToHashSet(), modelFactoryFullName: modelFactoryFullName, additionalNonRootTypeNames: nonRootTypes)
            {
                _rootFile = rootFile;
            }

            protected override Task<bool> IsRootDocument(Document document)
            {
                return document.Name == _rootFile ? Task.FromResult(true) : Task.FromResult(false);
            }
        }
