import { existsSync, readFileSync, writeFileSync } from "fs";
import { join } from "path";
import { PythonSdkContext } from "../../lib.js";
import { renderEnumFile } from "./handlebars.js";
import { collectEnumGroups, EnumGroup, UnsupportedEnumError } from "./render.js";

// Mirrors DEFAULT_HEADER_TEXT in generator/pygen/codegen/_utils.py
const DEFAULT_HEADER_TEXT =
  "Copyright (c) Microsoft Corporation. All rights reserved.\n" +
  "Licensed under the MIT License. See License.txt in the project root for license information.\n" +
  "Code generated by Microsoft (R) Python Code Generator.\n" +
  "Changes may cause incorrect behavior and will be lost if the code is regenerated.";

const HEADER_RULE = "# --------------------------------------------------------------------------";

function isAzureFlavor(context: PythonSdkContext): boolean {
  return (context.emitContext.options as any).flavor === "azure";
}

/** Mirrors `CodeModel.license_header` in generator/pygen/codegen/models/code_model.py. */
function computeLicenseHeader(context: PythonSdkContext): string {
  const options = context.emitContext.options as any;
  const licenseInfo = context.sdkPackage.licenseInfo as { header?: string } | undefined;
  let header: string;
  if (licenseInfo || !isAzureFlavor(context)) {
    // typespec unbranded case and azure case with custom license
    header = licenseInfo?.header ?? "";
  } else {
    // typespec azure case without custom license
    header = options["header-text"] || DEFAULT_HEADER_TEXT;
  }
  if (header) {
    header = header.replace(/\n/g, "\n# ");
    header = `${HEADER_RULE}\n# ${header}`;
    header += `\n${HEADER_RULE}`;
  }
  return header;
}

/** Mirrors `CodeModel.core_library` plus the enum import suffix in enum_container.py.jinja2. */
function computeCoreImport(context: PythonSdkContext): string {
  return isAzureFlavor(context) ? "azure.core" : "corehttp.utils";
}

/**
 * Replicate the standard-case `get_generation_dir` from pygen: the file lives at
 * `<outputDir>/<namespace parts>/models/_enums.py`. The namespace is already
 * lower-cased by `getClientNamespace`.
 */
function computeEnumFilePath(outputDir: string, namespace: string): string {
  return join(outputDir, ...namespace.split("."), "models", "_enums.py");
}

/** Parse the set of `class Name(...)` names declared in an existing `_enums.py`. */
function existingClassNames(content: string): Set<string> {
  const names = new Set<string>();
  const regex = /^class (\w+)\(/gm;
  let match: RegExpExecArray | null;
  while ((match = regex.exec(content)) !== null) {
    names.add(match[1]);
  }
  return names;
}

export interface RenderedEnumFile {
  /** Absolute path of the `_enums.py` file to overwrite. */
  path: string;
  /** TypeScript-rendered (pre-black) file content. */
  content: string;
  /** The class names rendered into the file (used as an under-collection guard). */
  classNames: string[];
}

/**
 * Pure render: compute the TypeScript-rendered `_enums.py` files (path + content)
 * for every namespace group sourced directly from TCGC. No filesystem access, so
 * it can be used both for the in-process overwrite and for the batch pipeline
 * (where rendering happens in a different process than the final write).
 *
 * Returns `[]` (leaving pygen output authoritative) for unsupported folder
 * layouts or when an enum uses an unsupported value type.
 */
export function renderEnumFiles(context: PythonSdkContext, outputDir: string): RenderedEnumFile[] {
  const options = context.emitContext.options as any;
  // Milestone 1 only supports the standard typespec folder layout.
  if (options["no-namespace-folders"] || options["generation-subdir"]) {
    return [];
  }

  let groups: EnumGroup[];
  try {
    groups = collectEnumGroups(context);
  } catch (error) {
    if (error instanceof UnsupportedEnumError) {
      // An unsupported value type slipped through: leave pygen output intact.
      return [];
    }
    throw error;
  }

  const licenseHeader = computeLicenseHeader(context);
  const coreImport = computeCoreImport(context);

  return groups.map((group) => ({
    path: computeEnumFilePath(outputDir, group.namespace),
    content: renderEnumFile(group, { licenseHeader, coreImport }),
    classNames: group.enums.map((e) => e.name),
  }));
}

/**
 * Overwrite each pygen-generated `_enums.py` with the TypeScript-rendered version.
 *
 * Runs after pygen has written the Python files but before `black`, so black
 * normalizes the rendered output. Returns the list of files that were replaced.
 *
 * Safety guards (skip + leave pygen output intact rather than mis-generate):
 * - the standard `_enums.py` path must already exist (skips legacy /
 *   `no-namespace-folders` / `generation-subdir` layouts);
 * - the set of classes we render must exactly match the set pygen wrote (guards
 *   against under-collection from TCGC).
 */
export function replaceEnumFiles(context: PythonSdkContext, outputDir: string): string[] {
  const replaced: string[] = [];
  for (const file of renderEnumFiles(context, outputDir)) {
    if (!existsSync(file.path)) {
      continue;
    }
    const existing = readFileSync(file.path, "utf-8");
    const renderedNames = new Set(file.classNames);
    const existingNames = existingClassNames(existing);
    if (
      renderedNames.size !== existingNames.size ||
      ![...renderedNames].every((n) => existingNames.has(n))
    ) {
      // Our view of the enums differs from pygen's: don't risk dropping any.
      continue;
    }
    writeFileSync(file.path, file.content, "utf-8");
    replaced.push(file.path);
  }
  return replaced;
}
