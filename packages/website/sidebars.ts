function createLibraryReferenceStructure(
  libName: string,
  labelName: string,
  hasLinterRules: boolean,
  extra: SidebarItemConfig[]
): SidebarItemConfig {
  const rules: SidebarItemConfig = {
    type: "category",
    label: "Rules",
    items: [
      {
        type: "autogenerated",
        dirName: `libraries/${libName}/rules`,
      },
    ],
  };
  return {
    type: "category",
    label: labelName,
    link: {
      type: "doc",
      id: `libraries/${libName}/reference/index`,
    },
    items: [
      {
        type: "autogenerated",
        dirName: `libraries/${libName}/reference`,
      },
      ...(hasLinterRules ? [rules] : []),
      ...(extra ?? []),
    ],
  };
}

