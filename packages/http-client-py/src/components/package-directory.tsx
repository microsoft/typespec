import { code, SourceDirectory, SourceFile } from "@alloy-js/core";
import { type Children } from "@alloy-js/core/jsx-runtime";
import * as py from "@alloy-js/python";

export interface PythonPackageDirectoryProps {
  /** Name of the Python distribution (as it will appear in pyproject.toml). */
  name: string;
  /** Version string for the distribution. */
  version: string;
  /** Path the package directory will be created at. */
  path: string;
  /** Top-level Python module name. Defaults to a snake_case version of `name`. */
  moduleName?: string;
  /**
   * Additional pyproject.toml dependencies, as a list of PEP 508 requirement strings.
   * `corehttp` and `isodate` are always included.
   */
  extraDependencies?: string[];
  children?: Children;
}

function toModuleName(name: string): string {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9_]+/g, "_")
    .replace(/^_+|_+$/g, "");
}

/**
 * Emits a Python package layout at `props.path`:
 *
 * ```
 * <path>/
 *   pyproject.toml
 *   README.md
 *   <moduleName>/
 *     __init__.py
 *     _version.py
 *     py.typed
 *     {children}
 * ```
 */
export function PythonPackageDirectory(props: PythonPackageDirectoryProps) {
  const moduleName = props.moduleName ?? toModuleName(props.name);
  const deps = ["corehttp>=1.0.0b6", "isodate>=0.6.1", ...(props.extraDependencies ?? [])];
  const depsToml = deps.map((d) => `  "${d}",`).join("\n");

  const pyproject = `[build-system]
requires = ["setuptools>=68", "wheel"]
build-backend = "setuptools.build_meta"

[project]
name = "${props.name}"
version = "${props.version}"
description = "Generated Python client for ${props.name}"
readme = "README.md"
requires-python = ">=3.9"
dependencies = [
${depsToml}
]

[tool.setuptools.packages.find]
include = ["${moduleName}*"]
`;

  const readme = `# ${props.name}\n\nThis package was generated by \`@typespec/http-client-py\`.\n`;

  return (
    <SourceDirectory path={props.path}>
      <SourceFile path="pyproject.toml" filetype="text">
        {pyproject}
      </SourceFile>
      <SourceFile path="README.md" filetype="text">
        {readme}
      </SourceFile>
      <SourceDirectory path={moduleName}>
        <py.SourceFile path="__init__.py">
          {code`from ._version import VERSION\n\n__version__ = VERSION\n`}
        </py.SourceFile>
        <py.SourceFile path="_version.py">{code`VERSION = "${props.version}"\n`}</py.SourceFile>
        <SourceFile path="py.typed" filetype="text">
          {""}
        </SourceFile>
        {props.children}
      </SourceDirectory>
    </SourceDirectory>
  );
}
