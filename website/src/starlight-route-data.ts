import { defineRouteMiddleware, type StarlightRouteData } from "@astrojs/starlight/route-data";
import { getCollection } from "astro:content";

type SidebarEntry = StarlightRouteData["sidebar"][number];
type SidebarLink = Extract<SidebarEntry, { type: "link" }>;

const base = import.meta.env.BASE_URL;
const releaseNotesPrefix = "release-notes/";

/** Returns true for sidebar groups that contain release notes entries. */
function isReleaseNotesGroup(
  entry: SidebarEntry,
): entry is Extract<SidebarEntry, { type: "group" }> {
  return (
    entry.type === "group" &&
    entry.entries.some((e) => e.type === "link" && e.href.startsWith(base + releaseNotesPrefix))
  );
}

/**
 * Starlight computes prev/next pagination and sidebar order from the autogenerated (alphabetical)
 * order of the `release-notes` directory, which is wrong for versioned slugs (e.g. `typespec-1-12-0`
 * sorts before `typespec-1-2-0`). This middleware reorders release notes chronologically by
 * `releaseDate` so the sidebar shows newest-first and the pagination points to the correct
 * previous (older) and next (newer) release.
 */
export const onRequest = defineRouteMiddleware(async (context) => {
  const route = context.locals.starlightRoute;
  if (!route.id.startsWith(releaseNotesPrefix)) return;

  // Map every release note href to its releaseDate (missing dates are treated as oldest).
  const releaseNotes = await getCollection("docs", (x) => x.id.startsWith(releaseNotesPrefix));
  const dateByHref = new Map<string, number>();
  for (const note of releaseNotes) {
    dateByHref.set(base + note.id + "/", note.data.releaseDate?.getTime() ?? 0);
  }
  const dateOf = (href: string) => dateByHref.get(href) ?? 0;

  // Sort the release notes sidebar group newest-first so the displayed order matches pagination.
  const group = route.sidebar.find(isReleaseNotesGroup);
  if (!group) return;
  const links = group.entries.filter((e): e is SidebarLink => e.type === "link");
  links.sort((a, b) => dateOf(b.href) - dateOf(a.href));
  group.entries = links;

  // Recompute pagination chronologically: previous = older release, next = newer release.
  // `links` is newest-first, so the neighbour after the current entry is older (previous) and the
  // neighbour before it is newer (next). No wrap-around at the ends.
  //
  // Match on the current route id rather than the `isCurrent` flag: the config adds a standalone
  // "Release Notes" quick-link to the latest release which Starlight marks as current instead of
  // the group's own entry, so `isCurrent` is not reliable here.
  const currentHref = base + route.id + "/";
  const currentIndex = links.findIndex((link) => link.href === currentHref);
  if (currentIndex === -1) return;
  route.pagination = {
    prev: links[currentIndex + 1],
    next: currentIndex > 0 ? links[currentIndex - 1] : undefined,
  };
});
