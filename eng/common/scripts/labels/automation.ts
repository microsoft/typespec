import { resolve } from "path";
import { stringify } from "yaml";
import { AreaPaths } from "../../config/areas.js";
import { AreaLabels } from "../../config/labels.js";
import { CheckOptions, repoRoot, syncFile } from "../common.js";
import {
  and,
  eventResponderTask,
  hasLabel,
  includesModifiedFiles,
  isAction,
  labelAdded,
  labelRemoved,
  not,
  or,
  payloadType,
} from "./policy.js";

const policyFolder = resolve(repoRoot, ".github", "policies");

export interface SyncLabelAutomationOptions extends CheckOptions {}

export async function syncLabelAutomation(options: SyncLabelAutomationOptions) {
  await syncIssueTriagePolicy(options);
  await syncPrTriagePolicy(options);
}

async function syncIssueTriagePolicy(options: CheckOptions) {
  const yaml = stringify(issueTriageConfig);
  const content = `# This file is generated by the sync-labels script DO NOT EDIT manually\n${yaml}`;
  const filename = resolve(policyFolder, "issues.triage.yml");
  await syncFile(filename, content, options);
}

async function syncPrTriagePolicy(options: CheckOptions) {
  const yaml = stringify(prTriageConfig);
  const content = `# This file is generated by the sync-labels script DO NOT EDIT manually\n${yaml}`;
  const filename = resolve(policyFolder, "pr.triage.yml");
  await syncFile(filename, content, options);
}

const issueTriageConfig = {
  id: "issues.triage",
  name: "New Issue Assign labels",
  description: "Assign labels to new issues",
  resource: "repository",
  disabled: false,
  configuration: {
    resourceManagementConfiguration: {
      eventResponderTasks: [
        eventResponderTask({
          description: "Adds `needs-triage` label for new unassigned issues",
          if: [payloadType("Issues"), isAction("Opened"), not(and(["isAssignedToSomeone"]))],
          then: [
            {
              addLabel: {
                label: "needs-triage",
              },
            },
          ],
        }),
        eventResponderTask({
          description: "Remove `needs-triage` label when an area label is added",
          if: [
            payloadType("Issues"),
            hasLabel("needs-triage"),
            "isOpen",
            or(Object.keys(AreaLabels).map((area) => labelAdded(area))),
          ],
          then: [
            {
              removeLabel: {
                label: "needs-triage",
              },
            },
          ],
        }),
        eventResponderTask({
          description: "Add `needs-triage` back when all area labels are removed",
          if: [
            payloadType("Issues"),
            not(hasLabel("needs-triage")),
            "isOpen",
            or(Object.keys(AreaLabels).map((area) => labelRemoved(area))),
            not(or(Object.keys(AreaLabels).map((area) => hasLabel(area)))),
          ],
          then: [
            {
              removeLabel: {
                label: "needs-triage",
              },
            },
          ],
        }),
      ],
    },
  },
};

const prTriageConfig = {
  id: "pr.triage",
  name: "Assign area labels to PRs",
  description: "Assign area labels to PR depending on path modified.",
  resource: "repository",
  disabled: false,
  configuration: {
    resourceManagementConfiguration: {
      eventResponderTasks: [
        eventResponderTask({
          description: "Assign area labels to PRs based on modified files",
          if: [payloadType("Pull_Request"), "isOpen"],
          then: Object.entries(AreaPaths).map(([label, files]) => {
            return {
              if: [includesModifiedFiles(files)],
              then: [
                {
                  addLabel: {
                    label,
                  },
                },
              ],
            };
          }),
        }),
      ],
    },
  },
};
