import { resolve } from "path";
import { stringify } from "yaml";
import { CheckOptions, removeFile, syncFile } from "../utils/common.js";
import { expandFolder } from "../utils/find-area-changed.js";
import {
  PolicyServiceConfig,
  addLabel,
  addReply,
  and,
  eventResponderTask,
  hasLabel,
  includesModifiedFiles,
  isAction,
  labelAdded,
  labelRemoved,
  not,
  or,
  payloadType,
} from "./policy.js";
import type { RepoConfig } from "./types.js";

const policyFolder = resolve(process.cwd(), ".github", "policies");

export interface SyncLabelAutomationOptions extends CheckOptions {}

export async function syncLabelAutomation(config: RepoConfig, options: SyncLabelAutomationOptions) {
  await syncPolicyFile(createIssueTriageConfig(config), options);
  await syncPolicyFile(createPrTriageConfig(config), options);

  const externalOwners = collectExternalOwners(config);
  if (externalOwners.length > 0) {
    await syncPolicyFile(createExternalReviewersConfig(config), options, externalOwners);
  } else {
    // No external owners to ping means the policy would have no tasks, so avoid
    // emitting an empty generated file (and remove any stale one).
    await removeFile(resolve(policyFolder, "prs.external-reviewers.generated.yml"), options);
  }
}

async function syncPolicyFile(
  policy: PolicyServiceConfig,
  options: CheckOptions,
  ignoreWords: string[] = [],
) {
  const yaml = stringify(policy);
  const cspell = ignoreWords.length > 0 ? `# cspell:ignore ${ignoreWords.join(" ")}\n` : "";
  const content = `# This file is generated by the sync-labels script DO NOT EDIT manually\n${cspell}${yaml}`;
  const filename = resolve(policyFolder, `${policy.id}.generated.yml`);
  await syncFile(filename, content, options);
}

function collectExternalOwners(config: RepoConfig): string[] {
  const all = new Set<string>();
  for (const owners of Object.values(config.externalOwners)) {
    for (const owner of owners) {
      all.add(owner);
    }
  }
  return [...all].sort();
}

function createIssueTriageConfig(config: RepoConfig): PolicyServiceConfig {
  const areaLabels = config.labels.area.labels;
  return {
    id: "issues.triage",
    name: "New Issue Assign labels",
    description: "Assign labels to new issues",
    resource: "repository",
    disabled: false,
    configuration: {
      resourceManagementConfiguration: {
        eventResponderTasks: [
          eventResponderTask({
            description: "Adds `needs-area` label for new unassigned issues",
            if: [
              payloadType("Issues"),
              isAction("Opened"),
              not(and(["isAssignedToSomeone"])),
              not(or(Object.keys(areaLabels).map((area) => hasLabel(area)))),
            ],
            then: [
              {
                addLabel: {
                  label: "needs-area",
                },
              },
            ],
          }),
          eventResponderTask({
            description: "Remove `needs-area` label when an area label is added",
            if: [
              payloadType("Issues"),
              hasLabel("needs-area"),
              "isOpen",
              or(Object.keys(areaLabels).map((area) => labelAdded(area))),
            ],
            then: [
              {
                removeLabel: {
                  label: "needs-area",
                },
              },
            ],
          }),
          eventResponderTask({
            description: "Add `needs-area` back when all area labels are removed",
            if: [
              payloadType("Issues"),
              not(hasLabel("needs-area")),
              "isOpen",
              or(Object.keys(areaLabels).map((area) => labelRemoved(area))),
              not(or(Object.keys(areaLabels).map((area) => hasLabel(area)))),
            ],
            then: [
              {
                addLabel: {
                  label: "needs-area",
                },
              },
            ],
          }),
        ],
      },
    },
  };
}
function createPrTriageConfig(config: RepoConfig): PolicyServiceConfig {
  return {
    id: "prs.triage",
    name: "Assign area labels to PRs",
    description: "Assign area labels to PR depending on path modified.",
    resource: "repository",
    disabled: false,
    configuration: {
      resourceManagementConfiguration: {
        eventResponderTasks: [
          eventResponderTask({
            if: [payloadType("Pull_Request")],
            then: Object.entries(config.areaPaths).map(([label, files]) => {
              const globs = files.map(expandFolder);
              return {
                if: [includesModifiedFiles(globs)],
                then: [
                  {
                    addLabel: {
                      label,
                    },
                  },
                ],
              };
            }),
          }),
        ],
      },
    },
  };
}
function createExternalReviewersConfig(config: RepoConfig): PolicyServiceConfig {
  const entries = Object.entries(config.externalOwners).filter(([, owners]) => owners.length > 0);
  return {
    id: "prs.external-reviewers",
    name: "Notify external area owners on PRs",
    description:
      "GitHub doesn't allow CODEOWNERS entries or formal review requests for users that are not repository collaborators, but some areas are contributed by external teams. Mention those owners in a comment instead. https://github.com/orgs/community/discussions/23042",
    resource: "repository",
    disabled: false,
    configuration: {
      resourceManagementConfiguration: {
        eventResponderTasks: [
          eventResponderTask({
            if: [payloadType("Pull_Request")],
            then: entries.map(([area, owners]) => {
              const globs = (config.areaPaths[area] ?? []).map(expandFolder);
              const mentions = owners.map((owner) => `@${owner}`).join(" ");
              // The area label doubles as the "already notified" guard: it is added
              // below the first time the paths match so owners are pinged once and not
              // on every subsequent push (the prs.triage policy also adds it, but adding
              // it here guarantees the guard is set even if that policy hasn't run yet).
              return {
                if: [includesModifiedFiles(globs), not(hasLabel(area)), not("isDraft")],
                then: [
                  addReply(
                    `${mentions} — this PR modifies files in the \`${area}\` area, which your team owns. Please take a look. (You can't be added as a formal reviewer because you're not a repository collaborator, so this is a heads-up instead.)`,
                  ),
                  addLabel(area),
                ],
              };
            }),
          }),
        ],
      },
    },
  };
}
